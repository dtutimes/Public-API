     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $news = SocietyNews::where(['status' => 'published'])->with(['society'])->orderBy('updated_at', 'desc')->get();

        $tempArr = [];

        foreach ($news as $item)
        {
            $item['imgUrl'] = $item->getFirstMediaUrl('soc_news_images');
            $item['society']['imgUrl'] = $item->society->getFirstMediaUrl('soc_logo');
            array_push($tempArr,$item);
        }

        return $tempArr;
    }

